module.exports = {
    add,
    sub,
    multiply,
    divide
}

num1 = 5;
num2 = 4;

function add(num1, num2) {
    return num1 + num2;
};

function sub(num1, num2) {
    return num1 - num2;
};

function multiply(num1, num2) {
    return num1 * num2;
};

function divide(num1, num2) {
    return num1 / num2;
};